#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	n;
	int	i;

	i = 0;
	n = 0;
	while (*s)
	{
		n++;
		s++;
	}
	while (i++ < n + 1)
	{
		if (*s == c)
		{
			return ((char *)s);
		}
		s--;
	}
	return (0);
}
